#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Switch,
    Case,
    Break,
    Return,
    Colon,
    Dot,
    Enum,
    LeftBrace,
    RightBrace,
    LeftParenthesis,
    RightParenthesis,
    LeftSquareBracket,
    RightSquareBracket,
    Default,
    Comma,
    AndKeyword,
    OrKeyword,
    Equals,
    Macro,
    Function,
    Constructor,
    Exit,
    New,
    Global,
    Globalvar,
    Mod,
    Try,
    With,
    Comment(String),
    Identifier(String),
    Real(f32),
    StringLiteral(String),
    LintTag(String),
    Eof,
}